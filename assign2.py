ultimate=0
penUltimate=1
print("1- "+ str(ultimate))
print("2- " + str(penUltimate))
i=2
sum=1
while i<11:
    next=ultimate+penUltimate
    print(str(i)+"- "+str(next))
    ultimate=penUltimate
    penUltimate=next
    sum=sum+next
    i=i+1
print("Sum is: "+ str(sum))
print("Average is: "+ str(sum/10))
